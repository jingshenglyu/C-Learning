#include<iostream>
using namespace std;

int main()
{
    cout << "The size of a char is:\t\t" << sizeof(char) << " byte.\n";
    cout << "The size of a bool is:\t\t" << sizeof(bool) << " byte.\n";
    cout << "The size of an int is:\t\t" << sizeof(int) << " byte.\n";
    cout << "The size of a short is:\t\t" << sizeof(short) << " byte.\n";
    cout << "The size of a long is:\t\t" << sizeof(long) << " byte.\n";
    cout << "The size of a float is:\t\t" << sizeof(float) << " byte.\n";
    cout << "The size of a double is:\t" << sizeof(double) << " byte.\n";

    return 0;
}